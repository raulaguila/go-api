package helper

func IntPtr(i int) *int {
	return &i
}

func StringPtr(s string) *string {
	return &s
}

func BoolPtr(b bool) *bool {
	return &b
}

func Float64Ptr(f float64) *float64 {
	return &f
}

func Float32Ptr(f float32) *float32 {
	return &f
}

func Uint64Ptr(u uint64) *uint64 {
	return &u
}
